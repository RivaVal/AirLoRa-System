    // ============================================================================
    /**
    * @brief 🔑 ОТЛАДКА: Печать PWM параметров сервопривода
    * @param servoIndex Индекс сервопривода (0-6)
    * @param angle Угол в градусах
    * @param pulseUs Ширина импульса в микросекундах
    * @param ticks Количество тиков PCA9685
    * 
    * @details Выводит в ESP_LOG:
    *   • Номер канала серво
    *   • Целевой угол
    *   • Рассчитанную ширину импульса (мкс)
    *   • Количество PWM тиков (0-4095)
    *   • Фактическую длительность в мкс
    * 
    * @note Вызывать из setLogicalAngle() для диагностики
    */
    void printPWMDebug(uint8_t servoIndex, int16_t angle, 
                    uint16_t pulseUs, uint16_t ticks);

    /**
    * @brief Обработка команд пульта с распределением по каналам PCA9685
    * @param comUp Команда тангажа/вспомогательных серво (0-180)
    * @param comLeft Команда рыскания (0-180)
    * @details Реализует распределение согласно ТЗ:
    *   - Каналы 0,1: Рули высоты (синхронно, реагируют на comUp)
    *   - Каналы 2,3: Рули направления (инверсно, реагируют на comLeft)
    *   - Каналы 4,5,6: Крышки/парашют (реагируют на comUp)
    * @note Все углы проходят внутреннюю валидацию validateSafetyAngle()
    */
    void processFlightCommands(uint8_t comUp, uint8_t comLeft);

    
private:
