    bool setPhysicalAngle(uint8_t servoIndex, uint16_t physicalAngle);
    //bool setPhysicalAngle(uint8_t channel, uint16_t physicalAngle);
    
    /**
     * @brief Прямая установка ширины импульса в микросекундах
     * @param channel Номер канала PCA9685 (0-15)
     * @param pulseUs Ширина импульса (500-2500 мкс для MG90S)
     *
     * @brief Прямая установка ширины импульса
     * @param servoIndex Индекс сервопривода (0-6)
     * @param pulseUs Ширина импульса в микросекундах
     * @return true при успехе
     */
    bool setPulseWidthUs(uint8_t servoIndex, uint16_t pulseUs);
    // bool setPulseWidthUs(uint8_t channel, uint16_t pulseUs);
    
    /**
     * @brief Установка PWM тиков напрямую (низкоуровневый метод)
     * @param channel Номер канала PCA9685 (0-15)
     * @param on_tick Тик включения (обычно 0)
     * @param off_tick Тик выключения (0-4095)
     * @return true при успехе
     * 
     * @note Это основной метод записи в регистры PCA9685
     */
    //bool setPWM(uint8_t channel, uint16_t on_tick, uint16_t off_tick);
    
    /**
     * @brief Установка калибровочной коррекции для канала
     * @param channel Номер канала PCA9685 (0-15)
     * @param trimUs Коррекция в микросекундах (-200...+200 мкс)
     *
     * @brief Установка калибровочной коррекции
     *
     * @param servoIndex Индекс сервопривода (0-6)
     * @param trimUs Коррекция в микросекундах (-200...+200)
     */
    void setServoTrim(uint8_t servoIndex, int16_t trimUs);
    //void setServoTrim(uint8_t channel, int16_t trimUs);
    
    /**
     * @brief Получение текущей калибровки канала
     * @param channel Номер канала PCA9685 (0-15)
     * @return Коррекция в микросекундах
     */
    int16_t getServoTrim(uint8_t channel) const;
    
    /**
     * @brief Сброс всех сервоприводов в нейтральное положение
     * @note Устанавливает 1500 мкс на все 7 каналов с задержкой 10 мс между ними
     */
    void resetToNeutral();
    
    /**
     * @brief 🔑 УТОЧНЁННЫЙ ПРЕДСТАРТОВЫЙ ТЕСТ СЕРВОПРИВОДОВ
     * @return true при успешном завершении теста
     * 
     * @details Последовательность теста (3 цикла):
     * • Все 7 каналов двигаются ОДНОВРЕМЕННО
     * • Последовательность: 0° → +30° → +60° → 0° → -30° → -60° → 0°
     * • Задержка между шагами: 600 мс
     * • Пауза между циклами: 1000 мс
     * • Финальный сброс в нейтраль
     * 
     * @note Вызывать в setup() после begin()
     */
    bool runServoTest();
    
    /**
     * @brief Проверка подключения PCA9685 по I2C
     * @return true если устройство отвечает на заданном адресе
     */
    bool isChipConnected() const { return _chipConnected; }
    
    /**
     * @brief Проверка инициализации контроллера
     * @return true если begin() был успешно выполнен
     */
    bool isInitialized() const { return _initialized; }
    
    /**
     * @brief Получение текущего I2C адреса устройства
     * @return Адрес PCA9685 (0x40 по умолчанию)
     */
    uint8_t getI2CAddress() const { return _i2cAddress; }
    
